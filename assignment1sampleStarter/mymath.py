def add_three(a, b, c):
    return a + b + c

def subtract_three(a, b, c):
    return a - b - c

def multiply_three(a, b, c):
    return a * b * c

def divide_three(a, b, c):
    if b != 0 and c != 0:
        return a / b / c
    else:
        return "Division by zero error"


def square(a):
    return a**2


def cube(a):
    return a**3
